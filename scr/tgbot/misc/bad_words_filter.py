def has_bad_words(text):
    bad_words = ['блядь', 'ебать', 'хуй', 'пизда', 'нахуй', 'сука', 'мудак', 'гандон',
                 'дрочить', 'дрочила', 'ебануться', 'ебашить', 'еблан', 'ебливый', 'заебать',
                 'заебись', 'залупа', 'мразь', 'хуесос', 'хуйня']
    for word in bad_words:
        if word in text.lower():
            return True
    return False
